# TIPOS DE DATOS

# Enteros                           ->      1, 2, 3, 4...
# Reales                            ->      1.5, 5.6...
# Caracteres                        ->      #, $, c, 5
# Cadenas de caracteres (String)    ->      "Hola", "Adios", "A"
# Booleanos                         ->      true, false


#Entero

Entero = 1

print("Este es tipo entero",Entero)

#Reales

Real = 1.5

print("Este es tipo Real",Real)


#Caracter

Caracter = "c$"

print("Este es tipo Caracter",Caracter)


#String

String = "Hola"

print("Este es tipo String",String)


#Boolean

Bool = True

print("Este es tipo Boolean",Bool)

